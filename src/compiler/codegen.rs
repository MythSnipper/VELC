use crate::compiler::parser::*;
use std::collections::{BTreeMap, HashMap};

#[derive(Default, Debug, Clone)]
pub struct FunctionFrame {
    pub name: String,
    pub stack_size: usize,
    pub next_local_offset: isize,
}

#[derive(Default, Debug, Clone)]
pub struct CodeGenerator {
    debug: bool,
    sections: BTreeMap<String, String>,

    label_id: usize,

    current_function: Option<FunctionFrame>,
    local_scopes: Vec<HashMap<String, isize>>,

    loop_stack: Vec<(String, String)>, // (continue_label, break_label)
}
impl CodeGenerator {
    pub fn new() -> Self {
        Self {
            debug: false,
            sections: BTreeMap::new(),

            label_id: 0,

            current_function: None,
            local_scopes: Vec::new(),

            loop_stack: Vec::new(),
        }
    }
    pub fn run(&mut self, program: &Program, debug: bool) -> Result<String, String> {
        self.debug = debug;
        
        self.emit_program(program)?;
    
        let mut out = String::from(";Generated by VELC\n");

        let ordered_sections = ["text", "data", "bss", "rodata"];

        for section in ordered_sections {
            if let Some(contents) = self.sections.get(section) {
                out.push_str(&format!("section .{}\n", section));
                out.push_str(contents);
                if !contents.ends_with('\n') {
                    out.push('\n');
                }
                out.push('\n');
            }
        }

        for (section, contents) in &self.sections {
            if ordered_sections.contains(&section.as_str()) {
                continue;
            }

            out.push_str(&format!("section .{}\n", section));
            out.push_str(contents);
            if !contents.ends_with('\n') {
                out.push('\n');
            }
            out.push('\n');
        }

        Ok(out)
    }

    fn next_label_id(&mut self) -> usize {
        self.label_id += 1;
        self.label_id
    }    

    fn push_section(&mut self, section: &str, text: &str) -> Result<(), String> {
        let section_name = match section {
            "" => "text",
            other => other,
        };

        let buf = self.sections.entry(section_name.to_string()).or_default();
        buf.push_str(text);
        if !text.ends_with('\n') {
            buf.push('\n');
        }

        Ok(())
    }
    fn debug_str(&mut self, text: &str, pad: &str) -> String {
        format!(";{pad} start\n{text};{pad} end\n")
    }

    fn type_size(&self, Type: &TypeName) -> Result<usize, String> {
        let ERROR_STRING_ROOT = "velc:CodeGenerator:type_size";
    
        match Type {
            TypeName::Builtin(BuiltinType::Bool) => Ok(1),
            TypeName::Builtin(BuiltinType::Int8) => Ok(1),
            TypeName::Builtin(BuiltinType::Uint8) => Ok(1),
            TypeName::Builtin(BuiltinType::Char) => Ok(1),
    
            TypeName::Builtin(BuiltinType::Int16) => Ok(2),
            TypeName::Builtin(BuiltinType::Uint16) => Ok(2),
    
            TypeName::Builtin(BuiltinType::Int32) => Ok(4),
            TypeName::Builtin(BuiltinType::Uint32) => Ok(4),
            TypeName::Builtin(BuiltinType::Float32) => Ok(4),
    
            TypeName::Builtin(BuiltinType::Int64) => Ok(8),
            TypeName::Builtin(BuiltinType::Uint64) => Ok(8),
            TypeName::Builtin(BuiltinType::Float64) => Ok(8),
    
            TypeName::Builtin(BuiltinType::String) => Ok(8), // pointer
            TypeName::Pointer(_) => Ok(8),
    
            _ => self.error(ERROR_STRING_ROOT, &format!("Unsupported global type size for {:?}", Type)),
        }
    }
    fn data_directive(&self, Type: &TypeName) -> Result<&'static str, String> {
        let ERROR_STRING_ROOT = "velc:CodeGenerator:data_directive";
    
        match self.type_size(Type)? {
            1 => Ok("db"),
            2 => Ok("dw"),
            4 => Ok("dd"),
            8 => Ok("dq"),
            n => self.error(ERROR_STRING_ROOT, &format!("Unsupported data size {}", n)),
        }
    }
    fn bss_directive(&self, Type: &TypeName) -> Result<&'static str, String> {
        let ERROR_STRING_ROOT = "velc:CodeGenerator:bss_directive";
    
        match self.type_size(Type)? {
            1 => Ok("resb"),
            2 => Ok("resw"),
            4 => Ok("resd"),
            8 => Ok("resq"),
            n => self.error(ERROR_STRING_ROOT, &format!("Unsupported bss size {}", n)),
        }
    }

    fn emit_static_initializer_value(&mut self, expr: &Expr, Type: &TypeName) -> Result<String, String> {
        let ERROR_STRING_ROOT = "velc:CodeGenerator:emit_static_initializer_value";
    
        match expr {
            Expr::BoolLiteral(v) => {
                Ok(if *v { "1".to_string() } else { "0".to_string() })
            }
    
            Expr::IntLiteral(v) => {
                Ok(v.to_string())
            }
    
            Expr::FloatLiteral(v) => {
                Ok(v.to_string())
            }
    
            Expr::CharLiteral(c) => {
                Ok((*c as u32).to_string())
            }
    
            Expr::StringLiteral(s) => {
                // string global stores pointer to rodata string
                let label = format!("_str_{}", self.next_label_id());
                self.push_section("rodata", &format!("{} db {:?}, 0\n", label, s));
                Ok(label)
            }
    
            Expr::Cast { expr, .. } => {
                self.emit_static_initializer_value(expr, Type)
            }
    
            Expr::Identifier(name) => {
                // only if you want symbol addresses as static initializers
                Ok(name.clone())
            }
    
            _ => {
                self.error(
                    ERROR_STRING_ROOT,
                    &format!("Unsupported static global initializer {:?}", expr),
                )
            }
        }
    }

    fn build_function_frame(&mut self, decl: &FunctionDecl) -> Result<FunctionFrame, String> {

        let mut locals_size = 0; //size locals take up in bytes
        let mut locals_count = 0; //number of locals

        for s in &decl.params {
            locals_size += self.type_size(&s.Type)?;
            locals_count += 1;
        }

        self.collect_function_locals(&decl.body, &mut locals_size, &mut locals_count)?;

        let stack_size = ((locals_size + 15) / 16) * 16; //16 byte alignment

        if self.debug {println!("Function {} has {} locals, {}B stack(real {}B)", decl.name, locals_count, stack_size, locals_size)}

        Ok(FunctionFrame{
            name: decl.name.clone(),
            stack_size: stack_size,
            next_local_offset: 0
        })
    }
    fn collect_function_locals(&mut self, stmt: &Stmt, offset: &mut usize, count: &mut u32) -> Result<(), String> {
        match stmt {
            Stmt::Block(stmts) => {
                for stmt in stmts {
                    self.collect_function_locals(stmt, offset, count)?;
                }
            }
    
            Stmt::VarDecl(decl) => {
                *offset += self.type_size(&decl.Type)?;
                *count += 1;
            }
    
            Stmt::If { then_branch, else_branch, .. } => {
                self.collect_function_locals(then_branch, offset, count)?;
                if let Some(else_branch) = else_branch {
                    self.collect_function_locals(else_branch, offset, count)?;
                }
            }
    
            Stmt::While { body, .. } => {
                self.collect_function_locals(body, offset, count)?;
            }
    
            Stmt::For { init, body, .. } => {
                if let Some(ForInit::VarDecl(decl)) = init {
                    *offset += self.type_size(&decl.Type)?;
                    *count += 1;
                }
                self.collect_function_locals(body, offset, count)?;
            }
            _ => {}
        }
    
        Ok(())
    }
    fn lookup_local_offset(&mut self, name: &str) -> Result<isize, String> {
        let ERROR_STRING_ROOT = "velc:CodeGenerator:lookup_local_offset";

        for scope in self.local_scopes.iter().rev() {
            if let Some(offset) = scope.get(name) {
                return Ok(*offset);
            }
        }

        self.error(ERROR_STRING_ROOT, &format!("Unknown local '{}'", name))
    }
    fn format_offset(&self, offset: isize) -> String {
        if offset < 0 {
            format!("-{}", -offset)
        }
        else {
            format!("+{}", offset)
        }
    }
    fn arg_register(&self, index: usize) -> Option<&'static str> {
        match index {
            0 => Some("rdi"),
            1 => Some("rsi"),
            2 => Some("rdx"),
            3 => Some("rcx"),
            4 => Some("r8"),
            5 => Some("r9"),
            _ => None,
        }
    }

    fn emit_program(&mut self, program: &Program) -> Result<(), String> {
        for item in &program.items {
            self.emit_top_level(item)?;
        }
        Ok(())
    }
    fn emit_top_level(&mut self, item: &TopLevel) -> Result<(), String> {

        match item {
            TopLevel::Function(decl) => {
                self.emit_function(decl)?;
            }
            TopLevel::GlobalVar(decl) => {
                self.emit_global_var(decl)?;
            }
            TopLevel::Assembly(decl) => {
                self.emit_top_level_asm(decl)?;
            }
        };

        Ok(())
    }
    fn emit_top_level_asm(&mut self, decl: &AssemblyDecl) -> Result<(), String> {

        let mut text = String::new();

        //inputs: move global values into regs
        let mut inputs_text = String::new();
        for i in &decl.inputs {
            inputs_text.push_str(&format!("mov {}, [{}]\n", i.reg, i.name));
        }
        if !inputs_text.is_empty() && self.debug {inputs_text = format!(";asm inputs: \n{inputs_text};-------\n");}

        //outputs: move regs into global values
        let mut outputs_text = String::new();
        for i in &decl.outputs {
            outputs_text.push_str(&format!("mov [{}], {}\n", i.name, i.reg));
        }
        if !outputs_text.is_empty() && self.debug {outputs_text = format!(";asm outputs: \n{outputs_text};-------\n");}

        text.push_str(&inputs_text);
        text.push_str(&decl.code);
        text.push_str(&outputs_text);

        self.push_section(&decl.section, &text)?;

        Ok(())
    }
    fn emit_global_var(&mut self, decl: &VarDecl) -> Result<(), String> {
        let ERROR_STRING_ROOT = "velc:CodeGenerator:emit_global_var";
    
        match &decl.init {
            Some(init) => {
                let dir = self.data_directive(&decl.Type)?;
                let value = self.emit_static_initializer_value(init, &decl.Type)?;
    
                self.push_section(
                    "data",
                    &format!("{} {} {}\n", decl.name, dir, value),
                )?;
    
                Ok(())
            }
    
            None => {
                let dir = self.bss_directive(&decl.Type)?;
    
                self.push_section(
                    "bss",
                    &format!("{} {} 1\n", decl.name, dir),
                )?;
    
                Ok(())
            }
        }
    }
    fn emit_function(&mut self, decl: &FunctionDecl) -> Result<(), String> {
        let ERROR_STRING_ROOT = "velc:CodeGenerator:emit_function";

        //Reset all local scopes
        self.local_scopes.clear();

        //calculates size of function locals
        let func = self.build_function_frame(decl)?;
        self.current_function = Some(func.clone());

        let end_label = format!("_{}_end", decl.name);

        //Function label
        self.push_section("text", &format!("global {}\n", decl.name))?;
        self.push_section("text", &format!("{}:\n", decl.name))?;

        //Function start stack frame
        self.push_section("text", "    endbr64\n")?;
        self.push_section("text", "    push rbp\n")?;
        self.push_section("text", "    mov rbp, rsp\n")?;

        //reserve space for locals
        if func.stack_size > 0 {
            self.push_section("text", &format!("    sub rsp, {}\n", func.stack_size))?;
        }

        //put register parameters into stack slots and add scoping
        self.local_scopes.push(HashMap::new());
        let parameter_scope: &mut HashMap<String, isize> = self.local_scopes.last_mut().unwrap();

        for (i, param) in decl.params.iter().enumerate() {
            let reg = match self.arg_register(i) {
                Some(reg) => reg,
                None => {
                    return self.error(
                        ERROR_STRING_ROOT,
                        "More than 6 parameters not yet supported",
                    );
                }
            };

            let offset = self.lookup_local_offset(&param.name)?;
            self.push_section(
                "text",
                &format!("    mov qword [rbp{}], {}\n", self.format_offset(offset), reg),
            )?;
        }

        //function body
        self.emit_stmt(&decl.body, &end_label)?;

        //return label
        self.push_section("text", &format!("{}:\n", end_label))?;

        //end stack frame
        self.push_section("text", "    mov rsp, rbp\n")?;
        self.push_section("text", "    pop rbp\n")?;
        self.push_section("text", "    ret\n")?;

        self.current_function = None;

        Ok(())
    }
    fn emit_stmt(&mut self, stmt: &Stmt, ret_label: &str) -> Result<(), String> {
        let ERROR_STRING_ROOT = "velc:CodeGenerator:emit_function";

        self.push_section("text", &format!("    jmp {}", ret_label));

        Ok(())
    }







    fn error<T>(&self, base: &str, err: &str) -> Result<T, String> {
        Err(format!("{base}:{err}\n"))
    }

}

